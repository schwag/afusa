        <a data-toggle="collapse" data-parent="#accordion" href="#collapseTwo">
          HOW LONG DOES POWDER COATING LAST?
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse">
      <div class="panel-body">
          <p>The life of the coating depends on the application. For corrosion and abrasion coatings, two months may suffice, however 2-3 years is not a-typical. For outdoor applications, five to seven years with minimal color and gloss reduction is usual, however for patio furniture, rust may occur at welds within 1-2 years. For electrical applications such as bus bars, coatings may last as long as 10-15 years. In general, the life expectancy of powder coating is longer than paint, making it a more reliable finish.</p>
