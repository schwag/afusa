      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseFour">
          HOW IS POWDER COATING APPLIED?
        </a>
      </h4>
    </div>
    <div id="collapseFour" class="panel-collapse collapse">
      <div class="panel-body">
          <p>All powder-coating applications begin with the fluidization of the powder. Finely ground plastic powder is placed in a reservoir that is attached to an air plenum. The plenum and reservoir are separated by a porous membrane, which allows only air to pass through it. When clean, compressed air enters the plenum, it moves through the membrane and aerates the powder, giving it a fluidized quality. In this fluidized state, the powder may be pumped like a liquid to a spray gun. There are two primary methods of powder coating: Electrostatic Spray and Dipping.</p>
          <p><b>Electrostatic Spray: </b>The most common method of powder application is electrostatic spray. The powder is pumped, using compressed air, from a fluidized reservoir through a spray gun. At the tip of the gun is a wire from an electrical power source, usually high voltage with micro amperage. The electricity from the wire creates a "corona" field area around the tip of the gun. When the powder passes through the corona field, the powder particles pick up an electrical charge. When these particles come in contact with a grounded metal part, they are electrostatically attracted to that part. The powder sticks to the part until the part can be placed in an oven where the powder melts and cures to the part.</p>
          <p><a href="https://www.youtube.com/watch?v=66GBenWs0c4" target="_blank"><b>Watch our video</b></a> on electro-static powder coating.</p>
          <p><b>Fluid Bed Dipping </b>Another method of application is called "fluid bed dipping". The part is heated to a temperature above the melting point of the plastic. The part is then dipped into the fluidized reservoir and the powder in contact with the part melts onto it. When the proper amount of powder has melted on to the part, the part is removed, any excess powder is blown off, and the part is placed in the oven for final curing.</p>
