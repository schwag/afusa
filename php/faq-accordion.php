        <a data-toggle="collapse" data-parent="#accordion" href="#collapseOne">
          HOW DOES POWDER DIFFER FROM PAINT?
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse in">
      <div class="panel-body">
          <p>Paint in general is a solid pigment in a solvent solution. It is applied wet and either air-dries or is baked dry in an oven. As the paint dries, the solvents evaporate, releasing any VOCâ€™c (volatile organic compounds) contained in the solvent, leaving a thin (less than 1mil) dry film of colored pigment.</p>
          <p>Powder coatings, in general, are finely ground plastic particles. The powder is fluidized using clean compressed air, and either pumped through a gun, electrically charged, and sprayed onto a cold, grounded part, or a hot part is dipped into the fluidized powder. In either case, the part is then placed into an oven where each particle of powder melts into the particles near it and together they flow into a uniform film, ranging from 1 to 20 mils thick.</p>
      </div>
