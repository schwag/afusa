      <h4 class="panel-title">
        <a data-toggle="collapse" data-parent="#accordion" href="#collapseSix">
          HOW DO THE POWDERS DIFFER?
        </a>
      </h4>
    </div>
    <div id="collapseSix" class="panel-collapse collapse">
      <div class="panel-body">
          <p>There are two main types of powder coatings: Thermoplastic and thermoset. Thermoplastic powders flow when they are heated, but do not fully cure, or crosslink. After they cool, if they are re-heated, they will re-soften. They never completely set up. Thermoset powders cure when they are heated, and their resins crosslink, finishing a chemical process that cannot be reversed.</p>
          <p>The basic powders are epoxy, urethane, nylon, vinyl, and floropolymers. Each of these resins has its inherent strengths and weaknesses, depending on the application.</p>
